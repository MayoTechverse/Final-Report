def load_sampled_cic_iot2023_dataset(base_path: str, rows_per_file: int = 100000) -> pd.DataFrame:
    """
    Load a manageable sample from each file instead of loading everything.
    """
    csv_files = glob.glob(os.path.join(base_path, "**", "*.csv"), recursive=True)
    parquet_files = glob.glob(os.path.join(base_path, "**", "*.parquet"), recursive=True)
    all_files = csv_files + parquet_files

    if len(all_files) == 0:
        raise FileNotFoundError(f"No CSV or parquet files found under: {base_path}")

    print(f"Found {len(all_files)} files.\n")

    dfs = []

    for file in all_files:
        filename = os.path.basename(file).lower()

        if "copy of" in filename:
            print(f"Skipping duplicate file: {file}")
            continue

        try:
            if file.lower().endswith(".csv"):
                df = pd.read_csv(file)
            else:
                df = pd.read_parquet(file)

            df.columns = df.columns.str.strip()

            if len(df) > rows_per_file:
                df = df.sample(n=rows_per_file, random_state=RANDOM_STATE)

            dfs.append(df)
            print(f"Loaded sampled: {file} -> {df.shape}")

        except Exception as e:
            print(f"Skipping: {file} | Error: {e}")

    if len(dfs) == 0:
        raise ValueError("No files could be loaded.")

    full_df = pd.concat(dfs, ignore_index=True)
    full_df.columns = full_df.columns.str.strip()

    print(f"\nCombined sampled dataset shape: {full_df.shape}")
    return full_df


def find_target_column(df: pd.DataFrame, preferred_target: str = "label") -> str:
    possible_targets = [
        preferred_target, "label", "Label", "labels", "Labels",
        "Attack", "attack", "Class", "class"
    ]

    df_cols_lower = {col.lower(): col for col in df.columns}

    for col in possible_targets:
        if col.lower() in df_cols_lower:
            actual_col = df_cols_lower[col.lower()]
            print(f"Using target column: {actual_col}")
            return actual_col

    raise ValueError(f"Could not find target column. Columns: {df.columns.tolist()}")


def inspect_dataset(df: pd.DataFrame, target_col: str):
    print("\n===== DATASET OVERVIEW =====")
    print("Shape:", df.shape)
    print("\nTarget value counts:")
    print(df[target_col].value_counts(dropna=False).head(30))
    print("\nMissing values (top 10):")
    print(df.isna().sum().sort_values(ascending=False).head(10))


def clean_dataset(df: pd.DataFrame, target_col: str) -> pd.DataFrame:
    df = df.copy()
    df.columns = df.columns.str.strip()

    raw_labels = df[target_col].astype(str).str.strip()
    print("\n===== RAW LABELS BEFORE BINARY MAPPING =====")
    print(raw_labels.value_counts().head(50))

    raw_labels_lower = raw_labels.str.lower()
    df[target_col] = raw_labels_lower.apply(lambda x: 0 if "benign" in x else 1)

    before = len(df)
    df = df.drop_duplicates()
    after = len(df)
    print(f"\nRemoved duplicates: {before - after}")

    df = df.replace([np.inf, -np.inf], np.nan)

    print("\n===== TARGET DISTRIBUTION AFTER CLEANING =====")
    print(df[target_col].value_counts())

    return df


def split_features_target(df: pd.DataFrame, target_col: str):
    X = df.drop(columns=[target_col])
    y = df[target_col]
    return X, y


def keep_numeric_features_only(X: pd.DataFrame):
    numeric_cols = X.select_dtypes(include=[np.number]).columns.tolist()
    X_numeric = X[numeric_cols].copy()
    return X_numeric, numeric_cols


def preprocess_features(X_train: pd.DataFrame, X_test: pd.DataFrame):
    numeric_cols = X_train.columns.tolist()

    imputer = SimpleImputer(strategy="median")
    scaler = StandardScaler()

    X_train_imputed = imputer.fit_transform(X_train)
    X_test_imputed = imputer.transform(X_test)

    X_train_scaled = scaler.fit_transform(X_train_imputed)
    X_test_scaled = scaler.transform(X_test_imputed)

    return X_train_scaled, X_test_scaled, numeric_cols, imputer, scaler


def mrmr_style_feature_selection(X_train, y_train, X_test, feature_names, top_k=20):
    mi_scores = mutual_info_classif(X_train, y_train, random_state=RANDOM_STATE)
    ranked_idx = np.argsort(mi_scores)[::-1][:top_k]

    selected_features = [feature_names[i] for i in ranked_idx]
    X_train_sel = X_train[:, ranked_idx]
    X_test_sel = X_test[:, ranked_idx]

    feature_df = pd.DataFrame({
        "Feature": feature_names,
        "MI_Score": mi_scores
    }).sort_values(by="MI_Score", ascending=False)

    feature_df.to_csv(OUTPUT_DIR / "feature_scores.csv", index=False)

    return X_train_sel, X_test_sel, selected_features, feature_df


def false_positive_rate(y_true, y_pred):
    cm = confusion_matrix(y_true, y_pred, labels=[0, 1])
    tn, fp, fn, tp = cm.ravel()
    return fp / (fp + tn) if (fp + tn) > 0 else 0.0


def evaluate_model(model, X_train, y_train, X_test, y_test, model_name):
    start_train = time.time()
    model.fit(X_train, y_train)
    train_time = time.time() - start_train

    start_infer = time.time()
    y_pred = model.predict(X_test)
    infer_time = time.time() - start_infer

    y_prob = None
    roc_auc = np.nan

    if hasattr(model, "predict_proba"):
        probs = model.predict_proba(X_test)
        if probs.shape[1] > 1:
            y_prob = probs[:, 1]
            roc_auc = roc_auc_score(y_test, y_prob)

    acc = accuracy_score(y_test, y_pred)
    prec = precision_score(y_test, y_pred, zero_division=0)
    rec = recall_score(y_test, y_pred, zero_division=0)
    f1 = f1_score(y_test, y_pred, zero_division=0)
    fpr = false_positive_rate(y_test, y_pred)
    cm = confusion_matrix(y_test, y_pred, labels=[0, 1])

    model_file = OUTPUT_DIR / f"{model_name}.joblib"
    joblib.dump(model, model_file)
    model_size_kb = os.path.getsize(model_file) / 1024

    results = {
        "Model": model_name,
        "Accuracy": acc,
        "Precision": prec,
        "Recall": rec,
        "F1": f1,
        "False Positive Rate": fpr,
        "ROC-AUC": roc_auc,
        "Training Time (s)": train_time,
        "Inference Time (s)": infer_time,
        "Model Size (KB)": model_size_kb
    }

    return results, y_pred, y_prob, cm


def plot_confusion_matrix(cm, model_name):
    disp = ConfusionMatrixDisplay(confusion_matrix=np.array(cm), display_labels=["Benign", "Attack"])
    disp.plot(cmap="Blues")
    plt.title(f"Confusion Matrix - {model_name}")
    plt.tight_layout()
    plt.savefig(OUTPUT_DIR / f"{model_name}_confusion_matrix.png")
    plt.show()


def plot_roc_curve(y_test, y_prob, model_name):
    if y_prob is None:
        return
    RocCurveDisplay.from_predictions(y_test, y_prob)
    plt.title(f"ROC Curve - {model_name}")
    plt.tight_layout()
    plt.savefig(OUTPUT_DIR / f"{model_name}_roc_curve.png")
    plt.show()


