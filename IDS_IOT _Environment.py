def main():
    df = load_sampled_cic_iot2023_dataset(BASE_DATASET_PATH, rows_per_file=ROWS_PER_FILE)

    actual_target_col = find_target_column(df, TARGET_COLUMN)
    inspect_dataset(df, actual_target_col)

    df = clean_dataset(df, actual_target_col)

    if df[actual_target_col].nunique() < 2:
        raise ValueError("Still only one class after cleaning.")

    X, y = split_features_target(df, actual_target_col)
    X, numeric_cols = keep_numeric_features_only(X)

    print(f"\nUsing {len(numeric_cols)} numeric features.")

    X_train, X_test, y_train, y_test = train_test_split(
        X, y,
        test_size=TEST_SIZE,
        random_state=RANDOM_STATE,
        stratify=y
    )

    print("\nTrain shape:", X_train.shape)
    print("Test shape:", X_test.shape)

    X_train_processed, X_test_processed, feature_names, imputer, scaler = preprocess_features(X_train, X_test)

    joblib.dump(imputer, OUTPUT_DIR / "imputer.joblib")
    joblib.dump(scaler, OUTPUT_DIR / "scaler.joblib")

    X_train_sel, X_test_sel, selected_features, feature_df = mrmr_style_feature_selection(
        X_train_processed, y_train, X_test_processed, feature_names, top_k=TOP_K_FEATURES
    )

    print("\n===== SELECTED FEATURES =====")
    for i, f in enumerate(selected_features, start=1):
        print(f"{i}. {f}")

    models = {
        "DecisionTree": DecisionTreeClassifier(max_depth=8, random_state=RANDOM_STATE),
        "RandomForest": RandomForestClassifier(n_estimators=100, max_depth=12, random_state=RANDOM_STATE, n_jobs=-1),
        "LogisticRegression": LogisticRegression(max_iter=1000, random_state=RANDOM_STATE),
        "GradientBoosting": GradientBoostingClassifier(n_estimators=50, learning_rate=0.1, max_depth=3, random_state=RANDOM_STATE)
    }

    results = []

    for model_name, model in models.items():
        print(f"\n===== TRAINING {model_name} =====")
        result, y_pred, y_prob, cm = evaluate_model(
            model, X_train_sel, y_train, X_test_sel, y_test, model_name
        )

        results.append(result)

        print(f"\nResults for {model_name}:")
        for key, value in result.items():
            print(f"{key}: {value}")

        plot_confusion_matrix(cm, model_name)
        plot_roc_curve(y_test, y_prob, model_name)

    results_df = pd.DataFrame(results)
    results_df.to_csv(OUTPUT_DIR / "model_results.csv", index=False)

    print("\n===== FINAL RESULTS TABLE =====")
    print(results_df.sort_values(by="F1", ascending=False))
    results.append(result)
